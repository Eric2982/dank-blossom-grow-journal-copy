package com.dankblossom;

import android.content.Context;
import android.webkit.JavascriptInterface;
import android.webkit.WebView;

import com.google.android.play.core.integrity.IntegrityManager;
import com.google.android.play.core.integrity.IntegrityManagerFactory;
import com.google.android.play.core.integrity.IntegrityTokenRequest;

/**
 * PlayIntegrityBridge
 *
 * Exposes a JavaScript interface ("AndroidPlayIntegrity") to the WebView so that
 * the React web app can request a Play Integrity token and forward it to the
 * Cloudflare Worker verification endpoint.
 *
 * Register this bridge in your Activity / Fragment that hosts the WebView:
 *
 *   webView.addJavascriptInterface(
 *       new PlayIntegrityBridge(this, webView, CLOUD_PROJECT_NUMBER),
 *       "AndroidPlayIntegrity"
 *   );
 *
 * CLOUD_PROJECT_NUMBER is the numeric Google Cloud project number associated
 * with your app in the Google Play Console (Settings → Integrity API).
 */
public class PlayIntegrityBridge {

    private final Context context;
    private final WebView webView;
    private final long cloudProjectNumber;

    public PlayIntegrityBridge(Context context, WebView webView, long cloudProjectNumber) {
        this.context = context.getApplicationContext();
        this.webView = webView;
        this.cloudProjectNumber = cloudProjectNumber;
    }

    /**
     * Called by the JavaScript layer to request an integrity token.
     *
     * Usage from JavaScript (see src/lib/playIntegrity.js):
     *   window.AndroidPlayIntegrity.requestIntegrityToken(nonce, callbackId)
     *
     * On success the native code calls back:
     *   window.__playIntegrityCallbacks[callbackId].resolve(token)
     *
     * On failure it calls back:
     *   window.__playIntegrityCallbacks[callbackId].reject(new Error(message))
     *
     * @param nonce      Base64url-encoded nonce generated by the web layer
     * @param callbackId Unique ID used to route the result back to the correct
     *                   Promise in the web layer
     */
    @JavascriptInterface
    public void requestIntegrityToken(final String nonce, final String callbackId) {
        IntegrityManager manager = IntegrityManagerFactory.create(context);

        IntegrityTokenRequest request = IntegrityTokenRequest.builder()
                .setNonce(nonce)
                .setCloudProjectNumber(cloudProjectNumber)
                .build();

        manager.requestIntegrityToken(request)
                .addOnSuccessListener(response -> {
                    String token = escapeJs(response.token());
                    String js = "window.__playIntegrityCallbacks['" + callbackId
                            + "'] && window.__playIntegrityCallbacks['" + callbackId
                            + "'].resolve('" + token + "');";
                    webView.post(() -> webView.evaluateJavascript(js, null));
                })
                .addOnFailureListener(e -> {
                    String message = escapeJs(
                            e.getMessage() != null ? e.getMessage() : "Unknown error");
                    String js = "window.__playIntegrityCallbacks['" + callbackId
                            + "'] && window.__playIntegrityCallbacks['" + callbackId
                            + "'].reject(new Error('" + message + "'));";
                    webView.post(() -> webView.evaluateJavascript(js, null));
                });
    }

    /** Escapes single quotes and backslashes so the string is safe inside JS literals. */
    private static String escapeJs(String s) {
        return s.replace("\\", "\\\\").replace("'", "\\'");
    }
}
